    private static final Entry[] table = new Entry[256];

    static void init() {
        for (int i = 0; i < symbols.length; i += 3) {
            int ch = symbols[i];
            int pattern = symbols[i + 1];
            int numBits = symbols[i + 2];

            addToTable(table, pattern, numBits, ch);
        }
    }

    static void addToTable(Entry[] table, int pattern, int numBits, int ch) {
        assert(numBits > 0);

        if (numBits < 8) {
            int sh = 8 - numBits;
            var sym = new Symbol(ch, numBits);

            for (int k = 0; k < (1 << sh); k++) {
                int x = ((pattern << sh) & 0xff) | k;
                table[x] = sym;
            }
        } else if (numBits == 8) {
            table[pattern] = new Symbol(ch, numBits);
        } else {
            int index = pattern >> (numBits - 8);
            var jump = table[index] instanceof Jump ? (Jump) table[index]: new Jump(new Entry[256]);
            addToTable(jump.table(), pattern & ((1 << (numBits - 8)) - 1), numBits - 8, ch);
            table[index] = jump;
        }
    }

    static {
        init();
    }

    public static byte[] decode(byte[] bytes) {
        var out = new ByteArrayOutputStream(bytes.length);
        var stream = new BitStream(bytes);

        Entry[] t = table;
        var eos = false;

        while (!eos) {
            switch (t[stream.getByte() & 0xff]) {
                case Symbol sym -> {
                    if (sym.ch() == EOS) {
                        eos = true;
                        break;
                    }
                    stream.consume(sym.numBits());
                    out.write(sym.ch());
                    t = table;
                }
                case Jump jmp -> {
                    stream.consume(8);
                    t = jmp.table();
                }

                case null -> {
                    throw new IllegalStateException("Huffman decode: malformed byte %x".formatted(stream.getByte()));
                }
            }
        }

        return out.toByteArray();
    }

    static class BitStream {
        private final byte[] array;
        private int offset = 0;
        private int index = 0;

        public BitStream(byte[] arr) {
            array = arr;
        }

        void consume(int numBits) {
            if (offset + numBits >= 8) {
                index++;
            }

            offset = (offset + numBits) % 8;
        }

        byte getByte() {
            int next = (index + 1 < array.length) ? (array[index + 1] & 0xff) : 0xff;
            int bits = (index < array.length) ? (array[index] & 0xff) : 0xff;
            return (byte) (((bits << offset) & 0xff) | (next >> (8 - offset)));
        }
    }
